const dIM=(y,m)=>new Date(y,m+1,0).getDate();
const fDow=(y,m)=>new Date(y,m,1).getDay();
const sD=(a,b)=>a.getFullYear()===b.getFullYear()&&a.getMonth()===b.getMonth()&&a.getDate()===b.getDate();

function CalendarGrid({year,month,plugin,today,selectedDate,onSelect,direction}){
  const dim=dIM(year,month),fw=fDow(year,month),fd=plugin.firstDayOfWeek,so=(fw-fd+7)%7;
  const pm=month===0?11:month-1,py=month===0?year-1:year,pd=dIM(py,pm);
  const weeks=[];let dc=1,nc=1;
  for(let w=0;w<6;w++){const row=[];for(let d=0;d<7;d++){const ci=w*7+d;if(ci<so)row.push({day:pd-so+ci+1,inM:false,date:new Date(py,pm,pd-so+ci+1)});else if(dc<=dim){row.push({day:dc,inM:true,date:new Date(year,month,dc)});dc++;}else{const nm=month===11?0:month+1,ny=month===11?year+1:year;row.push({day:nc,inM:false,date:new Date(ny,nm,nc)});nc++;}}weeks.push(row);if(dc>dim&&w>=4)break;}
  const an=direction>0?"slideLeft":direction<0?"slideRight":"fadeIn";
  const todayBadge=plugin.getTodayBadge?plugin.getTodayBadge():"";

  return(<div style={{animation:`${an} 0.2s ease-out`}}>
    <div style={{display:"grid",gridTemplateColumns:"repeat(7,1fr)"}}>
      {Array.from({length:7},(_,i)=>{const idx=(fd+i)%7;return(<div key={i} style={{textAlign:"center",fontSize:11,fontWeight:600,padding:"8px 0",color:idx===0||idx===6?"#b45309":"#9a9490",letterSpacing:1.5,textTransform:"uppercase"}}>{plugin.getWeekdayShortName(idx)}</div>);})}
    </div>
    {weeks.map((wk,wi)=>(<div key={wi} style={{display:"grid",gridTemplateColumns:"repeat(7,1fr)"}}>
      {wk.map((c,ci)=>{
        const info=plugin.getDateInfo(c.date);const isT=sD(c.date,today);const isS=selectedDate&&sD(c.date,selectedDate);
        const w2=dow(c.date);const isWe=w2===0||w2===6;
        let nc2=c.inM?"#1c1917":"#d6d3d1";
        if(c.inM&&info.isRestDay&&info.badge)nc2="#dc2626";
        else if(c.inM&&info.isWorkDay)nc2="#1c1917";
        else if(c.inM&&isWe)nc2="#b45309";
        return(<div key={ci} onClick={()=>onSelect(c.date)} title={info.lunarTooltip||""} style={{position:"relative",textAlign:"center",padding:"5px 2px 7px",minHeight:56,cursor:"pointer",borderRadius:8,transition:"background 0.1s",background:isS?"rgba(180,83,9,0.07)":isT?"rgba(180,83,9,0.03)":"transparent"}} onMouseEnter={e=>{if(!isS&&!isT)e.currentTarget.style.background="rgba(0,0,0,0.02)";}} onMouseLeave={e=>{if(!isS&&!isT)e.currentTarget.style.background="transparent";}}>
          {c.inM&&isT&&(<span style={{position:"absolute",top:2,left:3,fontSize:8,fontWeight:700,lineHeight:"13px",color:"#fff",background:"#b45309",borderRadius:3,padding:"0 3px",letterSpacing:0}}>{todayBadge}</span>)}
          {c.inM&&info.badge&&(<span style={{position:"absolute",top:2,right:3,fontSize:8,fontWeight:700,lineHeight:"13px",color:"#fff",background:info.badge==="休"?"#dc2626":"#ea580c",borderRadius:3,padding:"0 3px"}}>{info.badge}</span>)}
          <div style={{fontSize:15,fontWeight:isT?700:500,fontFeatureSettings:"'tnum'",color:isT?"#b45309":nc2,lineHeight:"28px"}}>{c.day}</div>
          {c.inM&&info.subText&&(<div style={{fontSize:10,lineHeight:"13px",marginTop:1,color:info.subColor||"#b8b0a8",fontWeight:info.subColor?600:400,whiteSpace:"nowrap",overflow:"hidden",textOverflow:"ellipsis",padding:"0 1px"}}>{info.subText}</div>)}
        </div>);
      })}
    </div>))}
  </div>);
}

