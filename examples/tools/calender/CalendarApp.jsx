// ─── 惯例推算引擎 ─────────────────────────────────────────────
//
// 适用于未公布年份（2027+）。规则摘要：
//
// ■ 元旦/清明/端午/中秋（法定1天）：
//   贯通双休日形成3天连休，采用"就近向后"原则——
//   周一→接前周末 Sat-Sun-Mon，无调休
//   周二→借前周六 Sun-Mon-Tue，前Sat=班
//   周三→仅放1天，不调休
//   周四→接后周末 Thu-Fri-Sat，后Sun=班
//   周五→Fri-Sat-Sun，无调休
//   周六→Sat-Sun-Mon（补休），无调休
//   周日→Sat-Sun-Mon（补休），无调休
//
// ■ 春节（法定5天，除夕—初四）：
//   从除夕起连休9天，贯通前后双休日。
//   若除夕前一天为周末则纳入连休起点。
//   不足部分从最近的周六/周日调休补齐。
//
// ■ 劳动节（法定2天，5/1—5/2）：
//   从5/1起连休5天，贯通双休日。
//   不足部分前后各借一个周末调休。
//
// ■ 国庆节（法定3天，10/1—10/3）：
//   从10/1起连休7天，贯通双休日。
//   若中秋节落在10/1—10/7内则合并为8天连休。
//   若中秋节紧邻国庆（如9/28—9/30），视情况合并。
// ───────────────────────────────────────────────────────────────

/**
 * 3天假推算（元旦/清明/端午/中秋）
 * 就近向后原则：优先向后贯通周末
 */
function predict3Day(festDate, name) {
  const result = {};
  const d = festDate;
  const w = dow(d); // 0=Sun 1=Mon ... 6=Sat

  if (w === 1) {
    const sat = new Date(d.getFullYear(), d.getMonth(), d.getDate() - 2);
    const sun = new Date(d.getFullYear(), d.getMonth(), d.getDate() - 1);
    result[dk(sat)] = { n: name, b: "休", t: "h" };
    result[dk(sun)] = { n: name, b: "休", t: "h" };
    result[dk(d)]   = { n: name, b: "休", t: "h" };
  } else if (w === 2) {
    const prevSat = new Date(d.getFullYear(), d.getMonth(), d.getDate() - 3);
    const sun = new Date(d.getFullYear(), d.getMonth(), d.getDate() - 2);
    const mon = new Date(d.getFullYear(), d.getMonth(), d.getDate() - 1);
    result[dk(prevSat)] = { n: name, b: "班", t: "w" };
    result[dk(sun)] = { n: name, b: "休", t: "h" };
    result[dk(mon)] = { n: name, b: "休", t: "h" };
    result[dk(d)]   = { n: name, b: "休", t: "h" };
  } else if (w === 3) {
    result[dk(d)] = { n: name, b: "休", t: "h" };
  } else if (w === 4) {
    const fri = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 1);
    const sat = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 2);
    const nextSun = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 3);
    result[dk(d)]   = { n: name, b: "休", t: "h" };
    result[dk(fri)] = { n: name, b: "休", t: "h" };
    result[dk(sat)] = { n: name, b: "休", t: "h" };
    result[dk(nextSun)] = { n: name, b: "班", t: "w" };
  } else if (w === 5) {
    const sat = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 1);
    const sun = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 2);
    result[dk(d)]   = { n: name, b: "休", t: "h" };
    result[dk(sat)] = { n: name, b: "休", t: "h" };
    result[dk(sun)] = { n: name, b: "休", t: "h" };
  } else if (w === 6) {
    const sun = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 1);
    const mon = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 2);
    result[dk(d)]   = { n: name, b: "休", t: "h" };
    result[dk(sun)] = { n: name, b: "休", t: "h" };
    result[dk(mon)] = { n: name, b: "休", t: "h" };
  } else {
    const prevSat = new Date(d.getFullYear(), d.getMonth(), d.getDate() - 1);
    const mon = new Date(d.getFullYear(), d.getMonth(), d.getDate() + 1);
    result[dk(prevSat)] = { n: name, b: "休", t: "h" };
    result[dk(d)]   = { n: name, b: "休", t: "h" };
    result[dk(mon)] = { n: name, b: "休", t: "h" };
  }
  return result;
}

function predictChunJie(year) {
  const chuyi = findChunJie(year);
  if (!chuyi) return {};
  const chuxi = new Date(chuyi.getFullYear(), chuyi.getMonth(), chuyi.getDate() - 1);
  const result = {};
  
  // 连休从除夕开始，共9天
  const totalDays = 9;
  for (let i = 0; i < totalDays; i++) {
    const d = new Date(chuxi.getFullYear(), chuxi.getMonth(), chuxi.getDate() + i);
    result[dk(d)] = { n: "春节", b: "休", t: "h" };
  }
  
  // 春节惯例：向前后各借一天（就近找周末）
  // 向前找
  let d1 = new Date(chuxi);
  d1.setDate(d1.getDate() - 1);
  while (dow(d1) !== 6 && dow(d1) !== 0) d1.setDate(d1.getDate() - 1);
  if (!result[dk(d1)]) result[dk(d1)] = { n: "春节", b: "班", t: "w" };
  
  // 向后找
  const lastDay = new Date(chuxi.getFullYear(), chuxi.getMonth(), chuxi.getDate() + totalDays - 1);
  let d2 = new Date(lastDay);
  d2.setDate(d2.getDate() + 1);
  while (dow(d2) !== 6 && dow(d2) !== 0) d2.setDate(d2.getDate() + 1);
  if (!result[dk(d2)]) result[dk(d2)] = { n: "春节", b: "班", t: "w" };
  
  return result;
}

function predictLaborDay(year) {
  const result = {};
  const may1 = new Date(year, 4, 1);
  const w = dow(may1);
  for (let i = 0; i < 5; i++) {
    const d = new Date(year, 4, 1 + i);
    result[dk(d)] = { n: "劳动节", b: "休", t: "h" };
  }
  let weInSpan = 0;
  for (let i = 0; i < 5; i++) {
    const dw = (w + i) % 7;
    if (dw === 0 || dw === 6) weInSpan++;
  }
  const needBorrow = Math.max(0, 5 - 2 - weInSpan);
  if (needBorrow >= 1) {
    let d1 = new Date(year, 4, 0);
    while (dow(d1) !== 6) d1.setDate(d1.getDate() - 1);
    result[dk(d1)] = { n: "劳动节", b: "班", t: "w" };
  }
  if (needBorrow >= 2) {
    let d2 = new Date(year, 4, 6);
    while (dow(d2) !== 6) d2.setDate(d2.getDate() + 1);
    result[dk(d2)] = { n: "劳动节", b: "班", t: "w" };
  }
  return result;
}

function predictGuoQing(year) {
  const result = {};
  const oct1 = new Date(year, 9, 1);
  const zhongqiu = findLunarDate(8, 15, year);
  const zhongqiuKey = zhongqiu ? dk(zhongqiu) : null;
  let overlap = false;
  if (zhongqiu) {
    const diff = (zhongqiu - oct1) / 864e5;
    if (diff >= 0 && diff < 7) overlap = true;
  }
  let adjacent = false;
  if (zhongqiu && !overlap) {
    const diff = (oct1 - zhongqiu) / 864e5;
    if (diff > 0 && diff <= 3) adjacent = true;
  }
  let totalDays = 7, legalDays = 3;
  if (overlap) { totalDays = 8; legalDays = 4; }
  else if (adjacent) {
    const gapDays = (oct1 - zhongqiu) / 864e5;
    totalDays = 7 + gapDays; legalDays = 4;
  }
  const startDate = (adjacent && zhongqiu) ? zhongqiu : oct1;
  const w = dow(startDate);
  for (let i = 0; i < totalDays; i++) {
    const d = new Date(startDate.getFullYear(), startDate.getMonth(), startDate.getDate() + i);
    const key = dk(d);
    const isZQ = key === zhongqiuKey;
    result[key] = { n: isZQ ? "中秋节" : "国庆节", b: "休", t: "h" };
  }
  let weInSpan = 0;
  for (let i = 0; i < totalDays; i++) {
    const dw = (w + i) % 7;
    if (dw === 0 || dw === 6) weInSpan++;
  }
  const needBorrow = Math.max(0, totalDays - legalDays - weInSpan);
  if (needBorrow >= 1) {
    let d1 = new Date(startDate);
    d1.setDate(d1.getDate() - 1);
    while (dow(d1) !== 0 && dow(d1) !== 6) d1.setDate(d1.getDate() - 1);
    result[dk(d1)] = { n: "国庆节", b: "班", t: "w" };
  }
  if (needBorrow >= 2) {
    const lastDay = new Date(startDate.getFullYear(), startDate.getMonth(), startDate.getDate() + totalDays - 1);
    let d2 = new Date(lastDay);
    d2.setDate(d2.getDate() + 1);
    while (dow(d2) !== 6 && dow(d2) !== 0) d2.setDate(d2.getDate() + 1);
    result[dk(d2)] = { n: "国庆节", b: "班", t: "w" };
  }
  if (needBorrow >= 3) {
    const lastDay = new Date(startDate.getFullYear(), startDate.getMonth(), startDate.getDate() + totalDays - 1);
    let d3 = new Date(lastDay);
    d3.setDate(d3.getDate() + 1);
    while (dow(d3) !== 6 && dow(d3) !== 0) d3.setDate(d3.getDate() + 1);
    d3.setDate(d3.getDate() + 1);
    while (dow(d3) !== 6 && dow(d3) !== 0) d3.setDate(d3.getDate() + 1);
    if (!result[dk(d3)]) result[dk(d3)] = { n: "国庆节", b: "班", t: "w" };
  }
  return result;
}

const predictionCache = {};

function getPredictedHolidays(year) {
  if (predictionCache[year]) return predictionCache[year];
  if (year < 2025) { predictionCache[year] = {}; return {}; }
  const result = {};
  const merge = (obj) => Object.assign(result, obj);
  merge(predict3Day(new Date(year, 0, 1), "元旦"));
  merge(predictChunJie(year));
  const qmDay = getQingmingDate(year);
  merge(predict3Day(new Date(year, 3, qmDay), "清明节"));
  merge(predictLaborDay(year));
  const duanwu = findLunarDate(5, 5, year);
  if (duanwu) merge(predict3Day(duanwu, "端午节"));
  const zhongqiu = findLunarDate(8, 15, year);
  if (zhongqiu) {
    const oct1 = new Date(year, 9, 1);
    const diff = (oct1 - zhongqiu) / 864e5;
    if (diff > 3 || diff < 0) merge(predict3Day(zhongqiu, "中秋节"));
  }
  merge(predictGuoQing(year));
  predictionCache[year] = result;
  return result;
}

/** Get holiday info: announced data first, then prediction for unannounced years.
 *  For dates in Dec, also check if they belong to next year's 元旦 arrangement. */
function getHoliday(dateKey, year) {
  // Direct lookup in ANNOUNCED (covers all years including cross-year 元旦)
  if (ANNOUNCED[dateKey]) return { ...ANNOUNCED[dateKey], predicted: false };
  // If both this year AND next year are announced, no need to predict
  // (cross-year dates like 12/30 for next year's 元旦 are already in ANNOUNCED)
  if (ANNOUNCED_YEARS.has(year)) return null;
  const predicted = getPredictedHolidays(year);
  return predicted[dateKey] ? { ...predicted[dateKey], predicted: true } : null;
}

