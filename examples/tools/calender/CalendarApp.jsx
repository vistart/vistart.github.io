function s2l(sy,sm,sd){
  const base=new Date(1900,0,31),tgt=new Date(sy,sm-1,sd);
  let off=Math.round((tgt-base)/864e5),ly=1900;
  for(ly=1900;ly<2101&&off>0;ly++)off-=lyd(ly);
  if(off<0)off+=lyd(--ly);
  const leap=llm(ly);let isL=false,lm=1;
  for(lm=1;lm<13&&off>0;lm++){
    if(leap>0&&lm===leap+1&&!isL){--lm;isL=true;off-=lld(ly);}
    else off-=lmd(ly,lm);
    if(isL&&lm===leap+1)isL=false;
  }
  if(off===0&&leap>0&&lm===leap+1){if(isL)isL=false;else{isL=true;--lm;}}
  if(off<0){off+=(isL?lld(ly):lmd(ly,--lm));}
  const ld=off+1,gi=(ly-4)%10,zi=(ly-4)%12;
  return{year:ly,month:lm,day:ld,isLeap:isL,ganZhi:STEMS[gi]+BRANCHES[zi],zodiac:ZODIAC[zi],monthName:(isL?"闰":"")+LMON[lm],dayName:LDAY[ld],isFirst:ld===1};
}

function findChunJie(gYear){
  for(let m=0;m<=1;m++){
    const dim=new Date(gYear,m+1,0).getDate();
    for(let d=1;d<=dim;d++){
      try{const l=s2l(gYear,m+1,d);if(!l.isLeap&&l.month===1&&l.day===1)return new Date(gYear,m,d);}catch{}
    }
  }
  return null;
}

function findLunarDate(lunarMonth,lunarDay,gYear){
  const startMonth=Math.max(0,lunarMonth-2);
  const endMonth=Math.min(11,lunarMonth+2);
  for(let m=startMonth;m<=endMonth;m++){
    const dim=new Date(gYear,m+1,0).getDate();
    for(let d=1;d<=dim;d++){
      try{const l=s2l(gYear,m+1,d);if(!l.isLeap&&l.month===lunarMonth&&l.day===lunarDay)return new Date(gYear,m,d);}catch{}
    }
  }
  return null;
}

